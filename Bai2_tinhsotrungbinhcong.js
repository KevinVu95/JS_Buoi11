// Input: 5 số thực
// Process:
// 1. lấy input, var numA,B,C,D,E lần lượt là 10 15 20 25 30;
// 2. tạo biến average trung bình cộng
// 3. Xuất kết quả
// Output: Trung bình cộng 5 số

var numA = 10;
var numB = 15;
var numC = 20;
var numD = 25;
var numE = 30;
var average;
average = (numA + numB + numC + numD + numE) / 5;
console.log("Trung bình cộng các số trên là:", average);