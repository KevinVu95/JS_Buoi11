// Input: Chiều dài, chiều rộng
// Process:
// 1. lấy input, đặt biến var height= 20, var width = 10;
// 2. tạo biến var area và var perimeter là biến chưa được tính;
// 3. Xuất kết quả
// Output: Diện tích hình chữ nhật (area) và chu vi (perimeter);

var height = 20;
var width = 10;
var area;
var perimeter;
area = width * height;
perimeter = (height + width) * 2;
console.log("Diện tích hình chữ nhật: ", area);
console.log("Chu vi hình chữ nhật:", perimeter);
